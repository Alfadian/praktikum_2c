class aritmatika:
    def __init__(self,a,b):
        self.a=a
        self.b=b
    def tambah(self):
        c=self.a+self.b
        return c
    def kurang(self):
        c=self.a-self.b
        return c
    def kali(self):
        c=self.a*self.b
        return c
    def bagi(self):
        c=self.a/self.b
        return c